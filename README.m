# ✨ Beautiful Glass Calculator

A modern and responsive calculator built using HTML, CSS, and JavaScript.

## 🚀 Features

- Basic arithmetic operations (+, -, ×, ÷)
- Modern glassmorphism design
- Smooth button animations
- Responsive layout
- Error handling
- Mobile-friendly interface

